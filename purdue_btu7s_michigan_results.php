							<img class="image_size2" id="article_image" src="Images\Article_Images\Michigan_7s_Team_Photo.jpg">
							<p> A clear, cool, and windy day saw the Boilers 7s arrive at the University of Michigan for their first conference qualifier 7s tournament. The boys knew they had their work cut out for them, but at the same time confidence was high.</p>
							<h5> Game 1: Bowling Green </h5>
							<p> A tough draw saw Purdue facing Bowling Green first, having lost to the Falcons 20-17 one week earlier.  Purdue came out strong, possessing the ball early and scoring first when Eremia Tapsell finished off an outstanding set of phases by dotting down in the corner. Tapsell could not convert the tough kick from the corner, but Purdue was out to an early 5-0 lead. Not long after, Ryan Vizek finished off another impressive showing of team 7s to score under the posts. Tapsell would nail the conversion and the Boilers went into the short break up 12-0. </p>
							<p> The offensive and defensive effort continued in the second half, with Tapsell scoring another try. Peter Hunt would add the final try at the whistle, taking the ball around the weak side of a Purdue scrum inside OSU's 22m to ground the ball. Purdue was on top of Pool D early with a 22-0 shutout victory. </p>
							<h5> Game 2: Ohio State </h5>
							<p> Purdue carried their momentum from game 1 into their second pool match against Ohio State, and another strong half saw Jordan Davis, Patrick Jenkinson, and Tapsell put in one try each, with Tapsell converting two of the three attempts to give Purdue a 19-0 lead at the half. </p>
							<p> The Boilermakers defense produced another stalwart effort, surrendering only a single try to the Buckeyes. Dion Crowder added a try (Tapsell conversion) to finish with a 26-7 Purdue victory. This win earned Purdue the top of the pool with a 2-0 record. </p>
							<h5> Game 3 - Cup Quarterfinals: Ohio State </h5>
							<p> The Cup Round Seeding matched Purdue up with Ohio State again. The Buckeyes mettle proved tougher the second time around with two unconverted tries in the first half. Purdue scored Jerad Stump try (Tapsell conversion) to go into halftime down 10-7. </p>
							<p> Purdue - with a big impact from Nico Ngon Kemoum substituting for an injured Patrick Jenkinson - surged back in the second half. Stump and Tapsell found themselves across the try line again (Tapsell 1-2 conversion), and despite a final Ohio State try with a narrowly-missed conversion, Purdue remained on top at the final whistle 19-17 and advanced to its second Cup Semifinal in four years of BTU7s Tournament play. </p>
							<h5> Game 4 - Cup Semifinals: Illinois </h5>
							<p> Illinois came out firing in the semi, and Purdue found themselves down early, 12-0 at half.  Jerad Stump added an unconverted try in the second, but it was not enough to get the job done. Illinois took the win 24-5, and relegated the Boilers to the 3rd place game against Michigan State. </p>
							<h5> Game 5 - 3rd Place Match: Michigan State </h5>
							<p> MSU presented a different challenge than Purdue had seen all day, with several large, bruising players. A hard fought first half resulted in the Spartans taking a 5-0 halftime lead. However, the Boilermakers dug deep with valuable series points on the line. Tapsell and Vizek each scored a try (Tapsell 2-2 conversions) and held off a Spartan rally to hold a 14-5 lead when the referee signaled the end of regulation. </p>
							<p> Third place on the day, and 4-1 overall, Purdue currently sits third in the conference standings. The 7s Boilermakers now turn their sights on recovering this week, and fighting for the championship this Saturday (4/11/15) at the second BTU7s Qualifier at the University of Iowa. Stay up-to date right here, and show your support on social media with the hashtags ‪#‎PurdueRugby7s‬ and #BTU7s. </p>
							<p> And, as always, BOILER UP!!! </p>
